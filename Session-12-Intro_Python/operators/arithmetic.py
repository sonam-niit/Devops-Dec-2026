num1=5
num2=3

print(f"Addition: {num1+num2}")
print(f"Subtraction: {num1-num2}")
print(f"Multiplication: {num1*num2}")
print(f"Division: {num1/num2}") # 5/3 = 1.666666
print(f"Floor Division: {num1//num2}") # 5//3=1
print(f"Reminder: {num1%num2}") # 5%3 = 2
print(f"Exponetial: {num1**num2}") # 5 ^ 3 = 5 * 5 * 5

